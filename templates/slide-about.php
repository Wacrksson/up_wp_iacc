              <h5 class="font-bold text-center">Nossa história</h5>
              <br>
              <p class="text-justify">
                Em outubro de 2002, um grupo de mulheres iniciou um plano de visitação a oncologia pediátrica em um hospital da
                região. Durante este tempo, eram contadas historias bíblicas às crianças com a intenção de dar esperança e
                reforçar a fé, estas histórias também se estendiam as mães com o mesmo propósito. Neste período foi percebido
                que tanto as crianças como as mães passavam certo desconforto durante o tratamento, visto que ao deslocarem-se
                de outro município não possuíam recursos para hospedarem-se em hotéis ou pousadas na cidade onde realizavam o
                tratamento, além disto, elas tinham que deixar toda a família em sua casa para poder se dedicar exclusivamente
                a criança em tratamento, fatores que interferiam no tratamento visto que um ambiente inóspito era criado.
              </p>
              <p class="text-justify">
                Para solucionar esse problema, em 2010, foi criado o Instituto de Apoio à Criança com Câncer (IACC): Uma organização
                não governamental, sem fins lucrativos, que atende as necessidades psicossociais dos portadores de câncer com idade
                entre zero e dezenove anos e seus responsáveis.
              </p>
